package com.company;

public class CWH_04_literals {
    public static void main (String[] args){
        byte age = 34; // 34 is a literal
        int age2 = 56;
        long ageDino = 45541122L; // long number
        short age3 = 87;
        boolean a = true;
        char ch = 'A';
        float f1 = 5.6f; // This is a floating point value
        double d1 = 4.66; // This is double
        System.out.println(age);
        String str = "Harry"; // String literal
        System.out.println(str);
    }
}
